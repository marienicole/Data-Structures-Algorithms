dict = {1:1, 2:2, 3:3, 4:4}
val = 3

dict[5] = 5 # adds five to the dictionary

dict.pop(val) # removes 3 from dictionary

dict.get(2) # will return value with key 2

dict.popitem() # removes random item?

dict.update(5, 2)

print(8 in dict) # will print false, 8 is not in dict. ONLY KEYS

dict = dict.sorted()

del dict # deletes entire dictionary!
